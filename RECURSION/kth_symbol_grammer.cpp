#include<bits/stdc++.h>
using namespace std;
int kthGrammar(int n, int k) {
      if(n==1&&k==1) return 0;
      int mid=pow(2,n-1)/2;
      if(k<=mid) return kthGrammar(n-1, k);
      else return !kthGrammar(n, k-mid);
    }
int main()
{
    int n,k;
    cout<<"Enter n and k: ";
    cin>>n>>k;
    cout<<"The k-th symbol in n-th grammar is: "<<kthGrammar(n,k)<<endl;
    return 0;
}